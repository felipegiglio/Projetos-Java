package Projetos_de_apredizagem;

public class uso_do_switch{
    public static void main(String[] args) {
        int valor = 1;

        switch(valor){
            case 1:
            System.out.println("Valor 1");
            break;
            case 2:
            System.out.println("Valor 1");
            break;
            default:
            System.out.println("O valor nao existe");
            break;
        }
    }
}